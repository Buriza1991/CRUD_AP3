export type MartialArt = 'jiujitsu' | 'muaythai';

export interface Address {
  street: string;
  number: string;
  complement?: string;
  neighborhood: string;
  city: string;
  state: string;
  zipCode: string;
}

export interface StudentInput {
  name: string;
  age: number;
  belt?: string;
  weight: number;
  martialArts: MartialArt[];
  phone: string;
  email: string;
  address: string | Address; // Permite ambos para compatibilidade
  startDate: Date;
  active: boolean;
}

export interface StudentResponse extends StudentInput {
  id: number;
  createdAt: Date;
  updatedAt: Date;
}

